library(stringr)

str(ggplot2::mpg)
mpg <- as.data.frame(ggplot2::mpg)

str(ggplot2::midwest)
midwest <- as.data.frame(ggplot2::midwest)

#----------------------------------------------------------------------------

# Q1. mpg 데이터의 cty(도시 연비)와 hwy(고속도로 연비) 간에 어떤 관계가 있는지 알아보려고 합니다. 
#x축은 cty, y축은 hwy로 된 산점도를 만들어 보세요.
ggplot(mpg, aes(x = cty, y = hwy)) + geom_point()
ggsave("result1.png")

#----------------------------------------------------------------------------

# Q2. 자동차 중에서 어떤 class(자동차 종류)가 가장 많은지 알아보려고 합니다.
# 자동차 종류별 빈도를 표현한 막대 그래프를 만들어 보세요.
ggplot(mpg, aes(x = class)) + geom_bar()
ggsave("result2.png")

#----------------------------------------------------------------------------

# Q3.미국 지역별 인구통계 정보를 담은 ggplot2 패키지의 midwest 데이터를 이용해서 전체 인구와 아시아인 인구 간에 어떤 관계가 있는지 알아보려고 합니다.
#x축은 poptotal(전체 인구), y축은 popasian(아시아인 인구)으로 된 산점도를 만들어 보세요. 
#전체 인구는 50만 명 이하, 아시아인 인구는 1만 명 이하인 지역만 산점도에 표시되게 설정하세요.
pop <- midwest %>% filter(poptotal<=5e+05 & popasian<=1e+04)
options(scipen = 99)
ggplot(pop, aes(x = poptotal, y = popasian)) + geom_point()
options(scipen = 0)
ggplot(pop, aes(x = poptotal, y = popasian)) + geom_point()
ggsave("result3.png")

#----------------------------------------------------------------------------

# Q4. class(자동차 종류)가 "compact", "subcompact", "suv"인 자동차의 cty(도시 연비)가 어떻게 다른지 비교해보려고 합니다. 
# 세 차종의 cty를 나타낸 상자 그림을 만들어보세요.
cl <- mpg %>% filter(class == "compact" | class == "subcompact" | class == "suv")
ggplot(cl, aes(x = class, y = cty)) + geom_boxplot()
ggsave("result4.png")

